// Inline-bundle each A4 edge function into a single self-contained entrypoint
// so we can deploy via Supabase MCP with just one file (no shared deps).
// Strategy:
//   1. Read each shared module, strip its `import` lines + leading metadata.
//   2. Read the function entrypoint, replace its `../_shared/*` import lines
//      with empty (the symbols become available via concatenation).
//   3. Concatenate: external imports first, then shared modules, then the
//      function body. Re-export anything the entrypoint relied on.
// Output: scripts/bundles/<fn>.bundle.ts
//
// This keeps the runtime code byte-for-byte equivalent to the original — we
// only collapse the file boundaries.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const root = resolve(__dirname, "..");
const sharedDir = resolve(root, "supabase/functions/_shared");
const outDir = resolve(__dirname, "bundles");
mkdirSync(outDir, { recursive: true });

// Order matters: dependencies before dependents.
const SHARED_FILES = ["gemini.ts", "a4-themes.ts", "a4-prompts.ts", "a4-prompt-engineer.ts"];

function readShared() {
  const map = new Map();
  for (const f of SHARED_FILES) {
    map.set(f, readFileSync(resolve(sharedDir, f), "utf8"));
  }
  return map;
}

// Strip everything that has to be promoted to the top of the bundle:
//  - `import "https://..."` (deno xhr polyfill, etc.) — collected and deduped.
//  - `import { foo } from "./other-shared.ts"` — internal cross-shared imports
//    we just drop because the symbols will be concatenated.
function processSharedSource(source) {
  const externalImports = [];
  const lines = source.split("\n");
  const out = [];
  for (const line of lines) {
    const importMatch = line.match(/^\s*import\s+["']([^"']+)["'];\s*$/);
    if (importMatch) {
      const url = importMatch[1];
      if (url.startsWith("https://") || url.startsWith("http://") || url.startsWith("npm:") || url.startsWith("jsr:")) {
        externalImports.push(line.trim());
      }
      // drop the line either way (relative ones get absorbed by the bundle)
      continue;
    }
    const fromMatch = line.match(/^\s*import\s+[\s\S]*?\s+from\s+["']([^"']+)["'];\s*$/);
    if (fromMatch) {
      const url = fromMatch[1];
      if (url.startsWith("https://") || url.startsWith("http://") || url.startsWith("npm:") || url.startsWith("jsr:")) {
        externalImports.push(line.trim());
        continue;
      }
      if (url.startsWith("./") || url.startsWith("../")) {
        // Internal: drop, symbols come via concatenation.
        continue;
      }
    }
    out.push(line);
  }
  return { externalImports, body: out.join("\n") };
}

// Same processing but for the function entrypoint. Multiline imports are
// handled (some entrypoints span imports across multiple lines).
function processEntrypoint(source) {
  const externalImports = [];
  // First, regex out every import statement (including multi-line `import { a,\n b } from "..."`).
  // We do a scan-and-replace in two passes.
  const importRegex = /import\s+(?:[\s\S]+?\s+from\s+)?["']([^"']+)["'];?\s*\n/g;
  const cleaned = source.replace(importRegex, (full, url) => {
    if (url.startsWith("https://") || url.startsWith("http://") || url.startsWith("npm:") || url.startsWith("jsr:")) {
      externalImports.push(full.trim());
      return "";
    }
    // relative: drop
    return "";
  });
  return { externalImports, body: cleaned };
}

function bundle(fnName) {
  const sharedMap = readShared();
  const sharedAll = SHARED_FILES.map((f) => {
    const { externalImports, body } = processSharedSource(sharedMap.get(f));
    return { f, externalImports, body };
  });

  const entrySrc = readFileSync(
    resolve(root, `supabase/functions/${fnName}/index.ts`),
    "utf8",
  );
  const entry = processEntrypoint(entrySrc);

  // Dedupe external imports across all sources, preserving first-seen order.
  const seenImports = new Set();
  const allExternals = [];
  for (const ex of [...sharedAll.flatMap((s) => s.externalImports), ...entry.externalImports]) {
    if (!seenImports.has(ex)) {
      seenImports.add(ex);
      allExternals.push(ex);
    }
  }

  const banner = `// Auto-bundled for Supabase MCP deploy. Source files (in order):\n`
    + `//   ${SHARED_FILES.map((f) => `_shared/${f}`).join(", ")}, ${fnName}/index.ts\n`
    + `// Generated by scripts/bundle_a4_for_deploy.mjs — do not hand-edit.\n`;

  const parts = [
    banner,
    allExternals.join("\n"),
    "",
    ...sharedAll.map((s) => `// === inlined: _shared/${s.f} ===\n${s.body}`),
    `// === inlined: ${fnName}/index.ts ===\n${entry.body}`,
  ];
  return parts.join("\n\n");
}

for (const fn of ["a4-callback", "a4-generate", "a4-fetch-url"]) {
  const bundled = bundle(fn);
  const outPath = resolve(outDir, `${fn}.bundle.ts`);
  writeFileSync(outPath, bundled, "utf8");
  console.log(`bundled ${fn}: ${bundled.length} chars -> ${outPath}`);
}
