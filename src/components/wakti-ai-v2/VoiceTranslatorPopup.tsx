// OPTIMIZED: Audio manager with instant playback capabilities
class OptimizedAudioManager {
  private currentAudio: HTMLAudioElement | null = null;
  private isPlaying: boolean = false;
  private audioContextUnlocked: boolean = false;
  private preloadedAudio: Map<string, HTMLAudioElement> = new Map();

  async unlockAudioContext(): Promise<void> {
    if (this.audioContextUnlocked) return;
    
    try {
      const silentAudio = new Audio('data:audio/mp3;base64,SUQzBAAAAAAAI1RTU0UAAAAPAAADTGF2ZjU4Ljc2LjEwMAAAAAAAAAAAAAAA');
      await silentAudio.play();
      silentAudio.pause();
      this.audioContextUnlocked = true;
      console.log('üîä Audio context unlocked successfully');
    } catch (error) {
      console.log('üîä Audio context unlock failed:', error);
    }
  }

  preloadAudio(key: string, base64Audio: string): void {
    try {
      const audio = new Audio(`data:audio/mpeg;base64,${base64Audio}`);
      audio.preload = 'auto';
      audio.load();
      this.preloadedAudio.set(key, audio);
      console.log('üîä Audio preloaded for key:', key);
    } catch (error) {
      console.error('üîä Audio preload failed:', error);
    }
  }

  async playAudioInstantly(base64Audio: string, cacheKey?: string): Promise<void> {
    return new Promise((resolve, reject) => {
      try {
        this.stopCurrentAudio();
        
        // Try to use preloaded audio first
        let audio: HTMLAudioElement;
        if (cacheKey && this.preloadedAudio.has(cacheKey)) {
          audio = this.preloadedAudio.get(cacheKey)!;
          console.log('üîä Using preloaded audio for instant playback');
        } else {
          audio = new Audio(`data:audio/mpeg;base64,${base64Audio}`);
          audio.preload = 'auto';
        }
        
        this.currentAudio = audio;
        audio.volume = 1.0;
        
        audio.onended = () => {
          console.log('üîä Audio playback completed');
          this.isPlaying = false;
          this.currentAudio = null;
          resolve();
        };
        
        audio.onerror = (error) => {
          console.error('üîä Audio playback error:', error);
          this.isPlaying = false;
          this.currentAudio = null;
          reject(new Error('Audio playback failed'));
        };

        this.isPlaying = true;
        const playPromise = audio.play();
        
        if (playPromise) {
          playPromise
            .then(() => {
              console.log('üîä Audio started playing instantly');
            })
            .catch((error) => {
              console.error('üîä Play failed:', error);
              this.isPlaying = false;
              this.currentAudio = null;
              reject(error);
            });
        }
        
      } catch (error) {
        console.error('üîä Audio setup error:', error);
        this.isPlaying = false;
        this.currentAudio = null;
        reject(error);
      }
    });
  }

  stopCurrentAudio() {
    if (this.currentAudio) {
      this.currentAudio.pause();
      this.currentAudio.currentTime = 0;
      this.currentAudio = null;
      this.isPlaying = false;
    }
  }

  isAudioPlaying(): boolean {
    return this.isPlaying;
  }

  clearPreloadedAudio() {
    this.preloadedAudio.clear();
  }
}

